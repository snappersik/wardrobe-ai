# Точка входа FastAPI приложения.
# Здесь создаётся экземпляр приложения, настраивается CORS,
# подключаются роутеры API и статические файлы.
# =============================================================================

# Импорт FastAPI фреймворка
from fastapi import FastAPI

# Импорт middleware для CORS (Cross-Origin Resource Sharing)
# Позволяет фронтенду с другого домена делать запросы к API
from fastapi.middleware.cors import CORSMiddleware

# Импорт роутеров API из модулей приложения
from app.api import api_router
from app.api import users, clothing, outfits

# Импорт движка базы данных для создания таблиц
from app.db.database import engine

# Импорт базового класса моделей SQLAlchemy
from app.models.models import Base

# =============================================================================
# СОЗДАНИЕ ЭКЗЕМПЛЯРА FASTAPI ПРИЛОЖЕНИЯ
# =============================================================================
# title - название API (отображается в документации Swagger)
# description - описание API
# version - версия API
app = FastAPI(
    title="Wardrobe AI API",
    description="AI-powered wardrobe management system",
    version="1.0.0"
)

# =============================================================================
# НАСТРОЙКА CORS (Cross-Origin Resource Sharing)
# =============================================================================
# CORS нужен чтобы React-фронтенд мог делать запросы к API.
# Без этой настройки браузер заблокирует запросы с другого origin.
app.add_middleware(
    CORSMiddleware,
    # Список разрешённых origins (адресов фронтенда)
    allow_origins=["http://localhost:3000"],  # Vite dev server
    # Разрешить отправку cookies (важно для JWT в cookie)
    allow_credentials=True,
    # Разрешённые HTTP методы (GET, POST, PUT, DELETE и т.д.)
    allow_methods=["*"],
    # Разрешённые HTTP заголовки
    allow_headers=["*"],
)

# =============================================================================
# ПОДКЛЮЧЕНИЕ РОУТЕРОВ API
# =============================================================================
# Роутеры группируют эндпоинты по функциональности.
# prefix="/api" - все маршруты начинаются с /api

# Роутер пользователей: регистрация, авторизация, профиль
app.include_router(users.router, prefix="/api")

# Роутер одежды: загрузка, просмотр, удаление вещей
app.include_router(clothing.router, prefix="/api")

# Роутер образов: создание, редактирование, удаление нарядов
app.include_router(outfits.router, prefix="/api")

# =============================================================================
# РАЗДАЧА СТАТИЧЕСКИХ ФАЙЛОВ (ЗАГРУЖЕННЫЕ ИЗОБРАЖЕНИЯ)
# =============================================================================
# Монтируем папку uploads для доступа к загруженным фото одежды.
# URL /uploads/filename.jpg вернёт файл из папки uploads/filename.jpg
from fastapi.staticfiles import StaticFiles
import os

# Создаём папку uploads если её нет
if not os.path.exists("uploads"):
    os.makedirs("uploads")

# Монтируем статические файлы по пути /uploads
app.mount("/uploads", StaticFiles(directory="uploads"), name="uploads")

# =============================================================================
# КОРНЕВОЙ ЭНДПОИНТ
# =============================================================================
# GET / - проверка что сервер работает
@app.get("/")
async def root():
    """
    Корневой эндпоинт для проверки работоспособности API.
    Возвращает простое сообщение что бекенд запущен.
    """
    return {"message": "Wardrobe AI Backend is running!"}

